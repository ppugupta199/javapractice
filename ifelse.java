import java.util.*;;

public class ifelse {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        int button = sc.nextInt();
        if (button == 1) {
            System.out.println("Hello ");
        } else if (button == 2) {
            System.out.println("Good moring");
        } else {
            System.out.println("GOOD night");
        }
    }
}
